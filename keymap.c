/***********************
* Custom key processes *
***********************/
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch(keycode) {
        case KBD_TOGG:
            if (record->event.pressed) {
                int newLayer = layer_state_is(_COLEMAK) ? _QWERTY : _COLEMAK;
                set_single_persistent_default_layer(newLayer);
                layer_move(newLayer);
            }
            return false;
    }
    return true;
}

